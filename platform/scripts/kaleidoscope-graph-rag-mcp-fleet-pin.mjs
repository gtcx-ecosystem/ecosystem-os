#!/usr/bin/env node
import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const ECOSYSTEM_ROOT = resolve(REPO, '..');
const OUT_REL = 'audit/evidence/kaleidoscope-graph-rag-mcp-strict-fleet-pin-latest.json';
const REPORT_REL = 'docs/business/research/kaleidoscope-ai/graph-rag-mcp-strict-fleet-pin-latest.md';
const OUT = join(REPO, OUT_REL);
const REPORT = join(REPO, REPORT_REL);
const WRITE = process.argv.includes('--write');
const JSON_OUT = process.argv.includes('--json');
const LOCAL_DATE_FORMATTER = new Intl.DateTimeFormat('en-CA', {
  timeZone: 'Africa/Johannesburg',
  year: 'numeric',
  month: '2-digit',
  day: '2-digit'
});

const FLEET = [
  'baseline-os',
  'bridge-os',
  'ecosystem-os',
  'gtcx-os',
  'markets-os',
  'compliance-os',
  'terminal-os',
  'ledger-ui',
  'terra-os',
  'exploration-os',
  'veritas-ai',
  'sensei-os',
  'griot-ai',
  'nyota-ai',
  'fabric-os',
  'canon-os',
  'agile-os',
  'ledger-os',
  'inspection-os',
  'venture-os'
];

function localDate(isoDate) {
  return LOCAL_DATE_FORMATTER.format(new Date(isoDate));
}

function git(repoRoot, args) {
  try {
    return execFileSync('git', ['-C', repoRoot, ...args], { encoding: 'utf8' }).trim();
  } catch (err) {
    return null;
  }
}

function statusEntries(repo, status) {
  if (!status) return [];
  const entries = status.split('\n').filter(Boolean);
  if (repo === 'ecosystem-os') return [];
  return entries;
}

function inspectRepo(repo) {
  const repoRoot = join(ECOSYSTEM_ROOT, repo);
  const present = existsSync(repoRoot);
  if (!present) {
    return {
      repo,
      path: `../${repo}`,
      present: false,
      git: false,
      head: null,
      branch: null,
      remote: null,
      dirty: null,
      statusEntries: null,
      sourceDate: null
    };
  }

  const status = git(repoRoot, ['status', '--porcelain']);
  const entries = statusEntries(repo, status);
  const head = repo === 'ecosystem-os' ? 'self:commit-containing-this-pin' : git(repoRoot, ['rev-parse', 'HEAD']);
  return {
    repo,
    path: `../${repo}`,
    present: true,
    git: Boolean(git(repoRoot, ['rev-parse', '--git-dir'])),
    head,
    branch: git(repoRoot, ['branch', '--show-current']) || git(repoRoot, ['rev-parse', '--abbrev-ref', 'HEAD']),
    remote: git(repoRoot, ['config', '--get', 'remote.origin.url']),
    dirty: status === null ? null : entries.length > 0,
    statusEntries: entries.length,
    sourceDate: statSync(repoRoot).mtime.toISOString()
  };
}

function buildWitness() {
  const generatedAt = new Date().toISOString();
  const repos = FLEET.map(inspectRepo);
  const missing = repos.filter((item) => !item.present).map((item) => item.repo);
  const notGit = repos.filter((item) => item.present && !item.git).map((item) => item.repo);
  const dirty = repos.filter((item) => item.dirty).map((item) => item.repo);
  const unpinned = repos.filter((item) => item.present && item.git && !item.head).map((item) => item.repo);

  return {
    schema: 'gtcx://ecosystem-os/kaleidoscope-ai/graph-rag-mcp-strict-fleet-pin/v1',
    generatedAt,
    date: localDate(generatedAt),
    repo: 'ecosystem-os',
    phase: 'graph-rag-mcp-strict-reproducibility',
    ok: missing.length === 0 && notGit.length === 0 && unpinned.length === 0,
    summary: {
      repoCount: repos.length,
      pinnedRepos: repos.filter((item) => item.head).length,
      missingRepos: missing.length,
      nonGitRepos: notGit.length,
      dirtyRepos: dirty.length,
      unpinnedRepos: unpinned.length,
      strictCommand: 'pnpm kaleidoscope:graph-rag-mcp:strict',
      topology: 'canonical side-by-side sibling repos under ecosystem root',
      use: 'Reproduce strict Graph/RAG/MCP gate by checking out these repo HEAD SHAs in canonical side-by-side topology.'
    },
    missingRepos: missing,
    nonGitRepos: notGit,
    dirtyRepos: dirty,
    unpinnedRepos: unpinned,
    repos
  };
}

function renderReport(witness) {
  const lines = [
    '---',
    'title: Kaleidoscope AI Graph/RAG/MCP strict fleet pin latest',
    'status: generated',
    `date: ${witness.date}`,
    'owner: ecosystem-os',
    'document_type: evidence-report',
    "tags: ['kaleidoscope-ai', 'graph', 'rag', 'mcp', 'strict-gate', 'reproducibility']",
    'review_cycle: on-change',
    '---',
    '',
    '# Kaleidoscope AI Graph/RAG/MCP strict fleet pin latest',
    '',
    '> Generated by `pnpm kaleidoscope:graph-rag-mcp:pin:write`.',
    '',
    '## Summary',
    '',
    `- Pin status: ${witness.ok ? 'pass' : 'fail'}`,
    `- Repos: ${witness.summary.pinnedRepos}/${witness.summary.repoCount} pinned`,
    `- Missing repos: ${witness.summary.missingRepos}`,
    `- Non-git repos: ${witness.summary.nonGitRepos}`,
    `- Dirty repos: ${witness.summary.dirtyRepos}`,
    `- Strict command: \`${witness.summary.strictCommand}\``,
    '',
    '## Reproduce',
    '',
    'Use these pins by checking out the listed repo `HEAD` SHAs as side-by-side sibling repos, then run `pnpm kaleidoscope:graph-rag-mcp:strict` from `ecosystem-os`.',
    '',
    '## Fleet pins',
    '',
    '| Repo | Branch | HEAD | Dirty | Remote |',
    '| --- | --- | --- | --- | --- |'
  ];

  for (const item of witness.repos) {
    lines.push(`| ${item.repo} | ${item.branch ?? 'missing'} | ${item.head ?? 'missing'} | ${item.dirty ?? 'missing'} | ${item.remote ?? 'missing'} |`);
  }

  lines.push('', '## Boundary', '');
  lines.push('This pin report is reproducibility evidence only. It does not authorize external use, deployment, publication, ticket creation, repo writes, or partner contact.');
  return `${lines.join('\n')}\n`;
}

function main() {
  const witness = buildWitness();
  if (JSON_OUT) {
    console.log(JSON.stringify(witness, null, 2));
    return;
  }

  if (WRITE) {
    mkdirSync(dirname(OUT), { recursive: true });
    mkdirSync(dirname(REPORT), { recursive: true });
    writeFileSync(OUT, `${JSON.stringify(witness, null, 2)}\n`);
    writeFileSync(REPORT, renderReport(witness));
  }

  console.log('\n=== kaleidoscope:graph-rag-mcp:pin:check ===\n');
  console.log(`pin: ${witness.ok ? 'pass' : 'fail'}`);
  console.log(`repos: ${witness.summary.pinnedRepos}/${witness.summary.repoCount}`);
  console.log(`missing: ${witness.summary.missingRepos}`);
  console.log(`non-git: ${witness.summary.nonGitRepos}`);
  console.log(`dirty: ${witness.summary.dirtyRepos}`);
  console.log(`strict-command: ${witness.summary.strictCommand}`);
  if (WRITE) {
    console.log(`witness: ${OUT_REL}`);
    console.log(`report: ${REPORT_REL}`);
  }
  for (const repo of witness.missingRepos) console.log(`missing-repo: ${repo}`);
  for (const repo of witness.nonGitRepos) console.log(`non-git-repo: ${repo}`);
  for (const repo of witness.unpinnedRepos) console.log(`unpinned-repo: ${repo}`);
  process.exitCode = witness.ok ? 0 : 1;
}

main();
